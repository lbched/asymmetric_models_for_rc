function L = PLTsymllk(x0,yt,n,T)
A=eye(6);
A(1,1)=(x0(1));
A(2,1)=(x0(2));
A(3,1)=(x0(3));
A(4,1)=(x0(4));
A(5,1)=(x0(5));
A(6,1)=(x0(6));
A(2,2)=(x0(7));
A(3,3)=(x0(8));
A(4,4)=(x0(9));
A(5,5)=(x0(10));
A(6,6)=(x0(11));
B=eye(6);
B(1,1)=(x0(12));
B(2,2)=(x0(13));
B(3,3)=(x0(14));
B(4,4)=(x0(15));
B(5,5)=(x0(16));
B(6,6)=(x0(17));
St=PLTsymrec(A,B,yt,n,T);
L=zeros(T,1);
for j=1:T
d=det(St(:,:,j));
a=trace(St(:,:,j)\yt(:,:,j));
L(j)=-0.5*log(d)-0.5*(a);
end
end